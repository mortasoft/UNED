    public void mostrarLista() {
        
        DefaultListModel listModel = new DefaultListModel();
        lista.setModel(listModel);
        
        Nodo tmp = miLista.getRaiz();
        
        while(tmp!=null){
            listModel.addElement(tmp.getDato().toString());
            tmp=tmp.getSiguiente();
        }

    }

